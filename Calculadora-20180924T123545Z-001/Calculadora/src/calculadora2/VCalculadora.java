    public void arithmetic_operation(){
    
        switch(calculation){
            case 1: //suma
                ans = num + Double.parseDouble(jTextField4.getText());
                calculadora.enviarMensaje(Double.toString(ans));
                jTextField4.setText(Double.toString(ans));
                
                break;
            
            case 2: //resta
                ans = num - Double.parseDouble(jTextField4.getText());
                calculadora.enviarMensaje(Double.toString(ans));
                jTextField4.setText(Double.toString(ans));
                                
                break;
                
            case 3:// multiplicacion
                ans = num * Double.parseDouble(jTextField4.getText());
                calculadora.enviarMensaje(Double.toString(ans));
                jTextField4.setText(Double.toString(ans));
                                
                break;
                
            case 4:// division
                ans = num / Double.parseDouble(jTextField4.getText());
                calculadora.enviarMensaje(Double.toString(ans));
                jTextField4.setText(Double.toString(ans));
                                
                break;
                
        }
    }
        
        
        
        
